#include "bits/stdc++.h"

#define endl '\n'
#define all(x) (x).begin(), (x).end()
#define sz(x) (int)(x).size()

using namespace std;
using ll = long long;

inline bool is_balanced(const string& expr)
{
    int balance = 0;
    for (auto c: expr) {
        if (c == '(') {
            balance++;
        } else if (c == ')') {
            balance--;
        }
        if (balance < 0) {
            return false;
        }
    }
    return balance == 0;
}

inline void solve() {
    int n, count_open = 0, count_close = 0;
    cin >> n;
    string s;
    char c;
    for (int i = 0; i < n && cin.get(c); i++) {
        if (c != '(' && c != ')' && c != '?') {
            i--;
            continue;
        }
        if (c == '(') {
            count_open++;
        } else if (c == ')') {
            count_close++;
        }
        s.push_back(c);
    }
    if (sz(s) & 1 || *s.begin() == ')' || *s.rbegin() == '(') {
        cout << ":(" << endl;
        return;
    }
    if (sz(s) == 2) {
        cout << "()" << endl;
        return;
    }
    count_open -= *s.begin() == '(';
    count_close -= *s.rbegin() == ')';
    *s.begin() = '(';
    *s.rbegin() = ')';
    count_open = (sz(s) - 2) / 2 - count_open;
    count_close = (sz(s) - 2) / 2 - count_close;

    if (count_open < 0 || count_close < 0) {
        cout << ":(" << endl;
        return;
    }

    auto p = s.begin() + 1;
    auto e = s.end();
    e -= 2;
    int balance = 0;

    while (true) {
        if (*p == '?') {
            if (count_open > 0) {
                *p = '(';
                count_open--;
            } else if (count_close > 0) {
                *p = ')';
                count_close--;
            }
        }
        if (*p == '(') {
            balance++;
        } else {
            balance--;
        }
        if (balance < 0) {
            cout << ":(" << endl;
            return;
        }
        if (p != e) {
            p++;
        } else {
            cout << s << endl;
            return;
        }
    }
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.precision(20);
    solve();
    return 0;
}
